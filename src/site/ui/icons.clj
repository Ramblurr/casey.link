(ns site.ui.icons
  (:require [clojure.java.io :as io]))

(defn read-iconset
  ([name]
   (let [fname (str "site/ui/icons/" name ".edn")]
     (if-let [c (io/resource fname)]
       (-> c slurp read-string)
       (throw (ex-info (format "No icon set %s found" fname) {:name name}))))))

(def ^:dynamic *default-iconset* "phosphor-regular")

(defn ico
  ([name]
   (ico name *default-iconset*))
  ([name iconset]
   (get
    (read-iconset iconset)
    name)))

(defn icon->fn [svg]
  (fn [{:keys [class] :or {class ""} :as opts}]
    (-> svg
        (update-in [1] #(merge % (dissoc opts :class)))
        (update-in [1 :class]
                   (fn [existing new]
                     (str new " " existing)) class))))
(defmacro deficon [name svg-var]
  `(def ~name (icon->fn ~svg-var)))

